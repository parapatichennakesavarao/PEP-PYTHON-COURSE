import math
num=16
print("The square root of", num, "is", math.sqrt(num))#square root function, prints 4.0
print(math.factorial(5))#factorial function, prints 120
print(math.pow(2,3))#power function, prints 8.0
nums=7.8
print(math.floor(nums))#prints 7
print(math.ceil(nums))#prints 8
print(math.fabs(-10))#prints 10.0
print(math.gcd(48,18))#greatest common divisor, prints 6
